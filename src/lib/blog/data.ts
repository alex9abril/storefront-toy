export const getRelatedArticles = (articleId: string, limit = 3) => {
	const article = blogData.articles.find((a) => a.id === articleId);
	if (!article) return [];

	return blogData.articles
		.filter((a) => a.id !== articleId && a.category?.id === article.category?.id)
		.slice(0, limit);
};

export const searchArticles = (query: string) => {
	const searchQuery = query.toLowerCase();
	return blogData.articles.filter(
		(article) =>
			article.title.toLowerCase().includes(searchQuery) ||
			article.excerpt.toLowerCase().includes(searchQuery) ||
			article.content.toLowerCase().includes(searchQuery) ||
			article.tags?.some((tag) => tag.toLowerCase().includes(searchQuery)),
	);
};
